public class Solution {
    public int movesToMakeZigzag(int[] nums) {
        int ans1 = 0, ans2 = 0;// å¶å¤§ï¼Œ å¥‡å¤§
        int[] nums1 = nums.clone();// å¥‡æ•°ç”¨
        for (int i = 0; i < nums.length; i++) {
            if ((i & 1) == 0) {// å¶æ•°ä½ç½®
                if (i + 1 < nums.length && nums[i] <= nums[i + 1]) {// åå¤§ï¼Œå¶æ•°å¤§ä¸æ»¡è¶³ï¼Œéœ€è¦å¢åŠ ans1
                    ans1 += nums[i + 1] - nums[i] + 1;
                    nums[i + 1] = nums[i] - 1;
                }
                if (i + 1 < nums.length && nums1[i] >= nums1[i + 1]) {// åå¤§ï¼Œå¥‡æ•°å¤§ä¸æ»¡è¶³ï¼Œéœ€è¦å¢åŠ ans2
                    ans2 += nums1[i] - nums1[i + 1] + 1;
                    nums1[i] = nums1[i + 1] - 1;
                }
            } else {// å¥‡æ•°ä½ç½®
                if (i + 1 < nums.length && nums[i] >= nums[i + 1]) {// åå¤§ï¼Œå¶æ•°å¤§ä¸æ»¡è¶³ï¼Œéœ€è¦å¢åŠ ans1
                    ans1 += nums[i] - nums[i + 1] + 1;
                    nums[i] = nums[i + 1] - 1;
                }
                if (i + 1 < nums.length && nums1[i] <= nums1[i + 1]) {// åå¤§ï¼Œå¥‡æ•°å¤§ä¸æ»¡è¶³ï¼Œéœ€è¦å¢åŠ ans2
                    ans2 += nums1[i + 1] - nums1[i] + 1;
                    nums1[i + 1] = nums1[i] - 1;
                }
            }
        }
        return Math.min(ans1, ans2);
    }
}
